package org.th.entity.enums;

/**
 * Types of feed sections in the personalized feed
 */
public enum FeedSectionType {
    /**
     * Personalized recommendations based on time, location, and preferences
     */
    FOR_YOU,

    /**
     * Trending shops in user's vicinity
     */
    TRENDING_NEARBY,

    /**
     * Recommendations based on user's favorite categories
     */
    BASED_ON_FAVORITES,

    /**
     * Recently added shops
     */
    NEW_SHOPS
}
